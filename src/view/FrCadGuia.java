    public void salvar() {
        String nome = txtNome.getText().trim();
        String email = txtEmail.getText().trim();
        String telefone = txtTelefone.getText().trim();
        String estado = txtEstado.getText().trim();

        if (nome.isEmpty() || email.isEmpty() || telefone.isEmpty() || estado.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Guia guia = new Guia();
        guia.setNome(nome);
        guia.setEmail(email);
        guia.setTelefone(telefone);
        guia.setEstado(estado);

        GuiaController controller = new GuiaController();
        try {
            if (controller.inserir(guia)) {
                JOptionPane.showMessageDialog(this, "Guia salvo com sucesso!");
                dispose(); // fecha a janela ap√≥s salvar
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar guia.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar guia: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

