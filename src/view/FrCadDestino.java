    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String nomeDestino = txtLugar.getText().trim();
        String nomeCidade = txtCidade.getText();

        if (nomeDestino.isEmpty()) {
            JOptionPane.showMessageDialog(this, "O nome do destino n√£o pode ser vazio.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Destino destino = new Destino();
        destino.setNome(nomeDestino);
        destino.setCidade(nomeCidade);

        DestinoController controller = new DestinoController();
        boolean sucesso = controller.inserir(destino);

        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Destino cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            txtLugar.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar o destino.", "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnSalvarActionPerformed

