    public void pesquisar() {
        //pega o modelo da grade com suas colunas o
        DefaultTableModel modeloTabela = (DefaultTableModel) tblConCliente.getModel();

        //limpa a grade setando o número de linhas para zero
        modeloTabela.setNumRows(0);

        //cria um UsuarioController para poder acessar os dados da tabela usuario
        ClienteController controller = new ClienteController();

        //consulta os usuáriose guarda a lista de usuários que encontrou
        ArrayList<Cliente> listarClientes = controller.listarTodos();

        //preencher a grade
        //percorre todos os usuários presentes na lista
        for (Cliente usu : listarClientes) {
            //cria um array onde cada posição é o valor das colunas da grade
            Object[] linha = {
                usu.getId(), //coluna 0
                usu.getNome(), //coluna 1
                usu.getEmail(), //coluna 2
                usu.getTelefone(),
                Utils.converterDateToString(usu.getDataNascimento()), 
                usu.getCpf(),
            };

            //adiciona o array com os dados do usuário na grade
            modeloTabela.addRow(linha);
        }
    }

