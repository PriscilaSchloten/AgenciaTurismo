    private void btnAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAlterarMouseClicked
       
        //verificat se tem uma liha da grade selecionada
        if (tblConCliente.getSelectedRow() != -1) {
            //se tiver selecionada uma linha pega o código da grade/linha
            int linhaSelecionada = tblConCliente.getSelectedRow();
            String textoCelula = tblConCliente.getValueAt(linhaSelecionada, 0).toString();

            //converter o texto da célula em inteiro
            int id = Integer.parseInt(textoCelula);

            //com o PkUsuario eu vou criar uma tela de alteração passando o PkUsuario
            //essa tela irá carregar os dados desse usuário para poder alterar
            FrAltCliente telaAlt = new FrAltCliente(null, rootPaneCheckingEnabled, id);

            telaAlt.setVisible(true);
            
            //vai fechar a janela e logo em seguida atualizar a pesquisa automaticamente
            pesquisar();
        }
    }//GEN-LAST:event_btnAlterarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        if (tblConCliente.getSelectedRow() != -1) {
            //se tiver selecionada uma linha pega o código da grade/linha
            int linhaSelecionada = tblConCliente.getSelectedRow();
            String textoCelula = tblConCliente.getValueAt(linhaSelecionada, 0).toString();

            //converter o texto da célula em inteiro
            int PkUsuario = Integer.parseInt(textoCelula);

            //com o PkUsuario eu vou chamar um método de deletar no controller
           ClienteController controller = new ClienteController();
            if (controller.excluir(PkUsuario)) {
                pesquisar();//pesquiso primeiro e depois aparece se foi deletado ou não
                JOptionPane.showMessageDialog(rootPane, "Deletado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não foi deldetado");

            }

        }
    }//GEN-LAST:event_btnExcluirMouseClicked

